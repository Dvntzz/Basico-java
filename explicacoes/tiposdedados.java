package explicacoes;


public class tiposdedados {
	
	public static void main(String[]args) {
		
		int myNum = 5;
		System.out.println("Este é o o  numero inteiro(int): " + myNum);
		
		float myFloatNum = 5.99f;
		System.out.println("Este é o valor de float: " + myFloatNum);
		
		char myLetter = 'D';
		System.out.println("Este é o valor de char: " + myLetter);
		
		boolean myBool = true;
		System.out.println("Este é o valor de boolean: " + myBool);
		
		String myText = "Hello Word";
		System.out.println("Este é o valor de String: " + myText);
	
	
	
	
	}

}
